#include "../inc/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] && s[i] != (char)c)
		i++;
	if ((char)c != '\0' && s[i] == '\0')
		return (0);
	return ((char *)&s[i]);
}
